HTML = '''<h1>Environmental Management Procedure</h1>
<p>Defines how [Company Name] identifies, controls, and monitors environmental aspects aligned with ISO 14001 and regulatory obligations.</p>
<h2>1. Purpose</h2>
<p>Prevent pollution, minimise environmental impacts, and ensure compliance across all project activities.</p>
<h2>2. Aspect &amp; Impact Management</h2>
<ul>
  <li>Identify environmental aspects during planning; document in project risk registers and EMPs.</li>
  <li>Assess significance and apply hierarchy of controls.</li>
  <li>Review aspects when scope changes, incidents occur, or annually.</li>
</ul>
<h2>3. Key Management Areas</h2>
<h3>3.1 Erosion &amp; Sediment Control</h3>
<p>Implement ESCP before earthworks; maintain controls (sediment fences, drains) and inspect after rainfall.</p>
<h3>3.2 Waste Management</h3>
<p>Apply waste hierarchy (avoid, reduce, reuse, recycle, dispose); segregate streams; use licensed contractors; maintain waste tracking records.</p>
<h3>3.3 Spill Prevention &amp; Response</h3>
<p>Store hazardous materials in bunded areas; provide spill kits; contain and report spills per incident procedure; dispose of waste correctly.</p>
<h3>3.4 Flora, Fauna &amp; Heritage</h3>
<p>Protect sensitive areas; implement exclusion zones; follow permit conditions; halt work and notify authorities if unexpected finds occur.</p>
<h2>4. Compliance &amp; Monitoring</h2>
<ul>
  <li>Maintain environmental obligations in QSE-6.1-REG-03; ensure permits/licences are current.</li>
  <li>Monitor performance via inspections, water/air/noise testing, and dashboard KPIs.</li>
  <li>Report non-compliance immediately; record corrective actions in QSE-10.3-REG-01.</li>
</ul>
<h2>5. Communication &amp; Training</h2>
<ul>
  <li>Brief crews on environmental controls during inductions and toolbox talks.</li>
  <li>Share alerts and lessons learned; engage stakeholders per community plans.</li>
</ul>
<h2>6. Records &amp; Integration</h2>
<ul>
  <li>Store EMPs, monitoring results, waste dockets, and approvals in document management module.</li>
  <li>Link environmental data to lots, incidents, and improvement actions for traceability.</li>
  <li>Include environmental performance in monthly reports and management review.</li>
</ul>
'''
