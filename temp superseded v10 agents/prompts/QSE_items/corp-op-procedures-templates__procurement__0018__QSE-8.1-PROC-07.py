HTML = '''<h1>Procurement &amp; Supplier Management Procedure</h1>
<p>Sets out the process for sourcing goods, services, and subcontracts that meet QSE and project requirements.</p>
<h2>1. Purpose</h2>
<p>Provide a structured approach for evaluating, selecting, and managing suppliers/subcontractors to minimise risk and ensure value.</p>
<h2>2. Supplier &amp; Subcontractor Pre-qualification</h2>
<ul>
  <li>Engage only pre-qualified suppliers listed in the Approved Supplier Register.</li>
  <li>Assess proportional to risk/value: WHS (performance, ISO 45001), environment (ISO 14001, incident history), quality (ISO 9001), financial capacity, insurance, technical capability, past performance.</li>
  <li>Apply enhanced assessment for high-risk trades (demolition, asbestos, cranes, etc.).</li>
</ul>
<h2>3. Tendering &amp; Contract Award</h2>
<ul>
  <li>Align processes with Austroads fairness and transparency principles.</li>
  <li>Define scope, QSE requirements, specs, and inspection hold points in purchase orders/subcontracts.</li>
  <li>Evaluate tenders on price and non-price criteria, including QSE performance.</li>
  <li>Ensure awards comply with delegations of authority.</li>
</ul>
<h2>4. Mobilisation &amp; Onboarding</h2>
<ul>
  <li>Verify licences, insurances, and documentation prior to site access.</li>
  <li>Conduct induction covering project expectations, QSE requirements, and reporting protocols.</li>
  <li>Establish communication and interface arrangements.</li>
</ul>
<h2>5. Performance Monitoring</h2>
<ul>
  <li>Hold regular reviews on progress, QSE compliance, and issues.</li>
  <li>Perform site inspections and audits to verify adherence to standards.</li>
  <li>Manage NCRs or corrective actions; document outcomes in supplier performance records.</li>
  <li>Complete Supplier Performance Report at contract completion to inform future tenders.</li>
</ul>
<h2>6. Records &amp; Integration</h2>
<ul>
  <li>Maintain prequalification data, contracts, reports, and correspondence in procurement module.</li>
  <li>Link supplier records to incidents, NCRs, improvement actions, and dashboards for visibility.</li>
  <li>Review supplier performance during management review and update approved register accordingly.</li>
</ul>
'''
