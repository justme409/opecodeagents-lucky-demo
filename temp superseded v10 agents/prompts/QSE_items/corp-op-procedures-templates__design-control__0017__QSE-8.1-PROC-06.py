HTML = '''<h1>Design &amp; Development Control Procedure</h1>
<p>Specifies how client-supplied and AustBuild-generated designs are reviewed, verified, and controlled for safety, quality, and compliance.</p>
<h2>1. Purpose</h2>
<p>Ensure designs used for construction are fit for purpose and managed through a systematic process even when AustBuild is not the principal designer.</p>
<h2>2. Management of Client-Supplied Design</h2>
<h3>2.1 Design Input Review</h3>
<ul>
  <li>Conduct multidisciplinary review before issuing “For Construction”.</li>
  <li>Assess constructability, Safety in Design (SFAIRP), environmental impacts, and quality completeness.</li>
  <li>Document findings in a Design Review Report and communicate outcomes to the client.</li>
</ul>
<h3>2.2 Design Verification</h3>
<ul>
  <li>Undertake independent verification by competent third-party when contract or risk profile requires.</li>
  <li>Maintain evidence of verification and close out any findings before construction.</li>
</ul>
<h2>3. Control of AustBuild-Generated Designs</h2>
<ul>
  <li>Temporary works (traffic management, formwork, excavation support, etc.) designed by competent personnel.</li>
  <li>Independently check and verify designs before use; include risk assessment in design package.</li>
  <li>Store design documents in controlled repository with version history.</li>
</ul>
<h2>4. Design Change Control</h2>
<ol>
  <li>Raise Design Change Request describing proposed modification.</li>
  <li>Evaluate impacts on safety, cost, schedule, and quality.</li>
  <li>Obtain approvals from client and Project Manager prior to implementation.</li>
  <li>Update drawings, models, and registers; notify stakeholders of changes.</li>
</ol>
<h2>5. Records &amp; Integration</h2>
<ul>
  <li>Maintain design review minutes, verification reports, change logs, and approvals in document management system.</li>
  <li>Link design data to risk registers, ITPs, and construction packages for traceability.</li>
  <li>Review design control performance during management review and audits.</li>
</ul>
'''
