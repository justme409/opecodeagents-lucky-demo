HTML = '''<h1>Internal Audit Procedure</h1>
<p>Defines the methodology for planning, conducting, reporting, and following up on internal audits to assure IMS effectiveness and continual improvement.</p>
<h2>1.0 Purpose</h2>
<p>Describe the process for monitoring conformity with ISO 9001, ISO 14001, and ISO 45001 via internal audits.</p>
<h2>2.0 Audit Programme & Integration</h2>
<ul>
  <li>QSE Manager maintains an annual audit programme using the audit management module.</li>
  <li>Risk-based scheduling prioritises high-risk/poor-performing processes using dashboard analytics (/dashboard).</li>
</ul>
<h2>3.0 Audit Process</h2>
<h3>3.1 Planning</h3>
<ul>
  <li>Assign lead auditor through competency records.</li>
  <li>Prepare digital audit plan with scope, objectives, criteria, schedule; distribute automatically to stakeholders.</li>
</ul>
<h3>3.2 Execution</h3>
<ul>
  <li>Hold opening meeting; gather evidence via interviews, observations, and document review.</li>
  <li>Capture findings digitally with timestamps and audit trails.</li>
</ul>
<h3>3.3 Reporting</h3>
<ul>
  <li>Classify findings (Major/Minor/OFI) in audit reporting module.</li>
  <li>Generate audit report and issue notifications; include trend metrics.</li>
</ul>
<h3>3.4 Follow-up</h3>
<ul>
  <li>Assign corrective actions through corrective action module and track in QSE-10.3-REG-01.</li>
  <li>Lead auditor verifies effectiveness before closure.</li>
</ul>
<h2>4.0 Auditor Competency</h2>
<ul>
  <li>Auditors complete recognised training; records held in QSE-7.2-REG-01.</li>
  <li>Lead auditors require additional qualifications; system alerts manage renewals.</li>
</ul>
'''
