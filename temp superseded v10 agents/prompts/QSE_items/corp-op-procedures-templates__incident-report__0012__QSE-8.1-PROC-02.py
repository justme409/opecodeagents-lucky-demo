HTML = '''<h1>Incident Reporting &amp; Investigation Procedure</h1>
<p>Outlines how incidents are reported, classified, investigated, and closed in line with ISO 45001 and company requirements.</p>
<h2>1. Purpose</h2>
<p>Ensure all QSE incidents (injury, illness, property damage, environmental, near miss) are captured, analysed, and addressed promptly to prevent recurrence and share lessons learned.</p>
<h2>2. Reporting</h2>
<ul>
  <li>Report incidents to the immediate supervisor as soon as practicable.</li>
  <li>Supervisor records initial report in the incident module within 24 hours.</li>
  <li>Notifiable incidents are escalated immediately to regulators by the Project Manager or QSE Manager.</li>
  <li>System assigns severity rating and triggers notifications to duty holders.</li>
</ul>
<h2>3. Investigation</h2>
<ul>
  <li>Investigation level matches actual or potential severity.</li>
  <li>High/Extreme incidents require ICAM led by trained investigator; moderate incidents follow simplified root cause analysis.</li>
  <li>Collect evidence, witness statements, photos, and relevant records.</li>
  <li>Document findings, root causes, and contributing factors in the incident record.</li>
</ul>
<h2>4. Corrective &amp; Preventive Actions</h2>
<ul>
  <li>Create corrective actions linked to the incident; assign owners and due dates.</li>
  <li>Track completion and effectiveness via QSE-10.3-REG-01.</li>
  <li>Update risk registers, SWMS, or procedures where required.</li>
  <li>Close actions only after verifying effectiveness and communicating outcomes.</li>
</ul>
<h2>5. Communication &amp; Lessons Learned</h2>
<ul>
  <li>Share key learnings through toolbox talks, alerts, and management meetings.</li>
  <li>Record lessons learned for future project reference and continuous improvement.</li>
</ul>
<h2>6. Records &amp; Integration</h2>
<ul>
  <li>Incident records stored in the QSE incident module with full audit trail.</li>
  <li>Metrics (frequency, severity, closure times) reported on `/dashboard`.</li>
  <li>Link incidents to related lots, NCRs, or actions for traceability.</li>
  <li>Ensure privacy and regulatory compliance when handling personal data.</li>
</ul>
'''
