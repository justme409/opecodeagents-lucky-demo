HTML = '''<h1>WHS Management Procedure</h1>
<p>Framework for managing workplace health and safety risks, aligning with ISO 45001 and legal requirements.</p>
<h2>1. Purpose</h2>
<p>Provide mandatory processes to eliminate or minimise work-related injury and illness across corporate and project operations.</p>
<h2>2. WHS Risk Management</h2>
<ul>
  <li>Identify and assess hazards using hierarchy of controls.</li>
  <li>Maintain project risk registers with detailed controls in SWMS for high-risk tasks.</li>
  <li>Review controls regularly and after incidents or significant changes.</li>
</ul>
<h2>3. Consultation &amp; Participation</h2>
<ul>
  <li>Engage workers through Health &amp; Safety Committees, daily pre-starts, toolbox talks, and SWMS development.</li>
  <li>Record consultation activities in relevant modules for audit trail.</li>
</ul>
<h2>4. Training &amp; Competence</h2>
<ul>
  <li>Ensure personnel complete mandatory WHS training captured in QSE-7.2-REG-01.</li>
  <li>Verify competencies before allocating high-risk work; maintain VOC records.</li>
</ul>
<h2>5. Incident Management</h2>
<ul>
  <li>Report all incidents and near misses immediately; log in incident module within 24 hours.</li>
  <li>Investigate based on severity, using ICAM for high/critical events.</li>
  <li>Implement corrective actions via QSE-10.3-REG-01 and verify effectiveness.</li>
</ul>
<h2>6. Compliance &amp; Monitoring</h2>
<ul>
  <li>Monitor WHS KPIs (lead/lag indicators) on `/dashboard` and project reports.</li>
  <li>Conduct regular inspections, audits, and leadership walks; document findings and follow-up.</li>
  <li>Manage regulatory notifications and obligations through the compliance register.</li>
</ul>
<h2>7. Records &amp; Integration</h2>
<ul>
  <li>Store WHS documents, SWMS, and records in the document management module with controlled access.</li>
  <li>Link WHS data to project lots, NCRs, training records, and improvement actions for traceability.</li>
  <li>Review performance during management review and drive continual improvement.</li>
</ul>
'''
