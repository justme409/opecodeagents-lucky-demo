HTML = '''<h1>Construction &amp; Operational Control Procedure</h1>
<p>Defines how construction activities are planned, authorised, and executed to control QSE risks and meet specification requirements.</p>
<h2>1. Purpose</h2>
<p>Ensure all site works are controlled, compliant, and aligned with project objectives, preventing harm to people, environment, and quality outcomes.</p>
<h2>2. Risk-Based Work Planning</h2>
<p>Use layered documentation to plan work safely and effectively.</p>
<h3>2.1 Safe Work Method Statements (SWMS)</h3>
<ul>
  <li>Required for all High Risk Construction Work per WHS Regulations.</li>
  <li>Develop collaboratively with workers; identify steps, hazards, and controls.</li>
  <li>Supervisor reviews/approves before work; all workers sign on prior to commencement.</li>
  <li>Update when conditions change; store signed copies in the system.</li>
</ul>
<h3>2.2 Inspection &amp; Test Plans (ITPs)</h3>
<ul>
  <li>Prepare ITPs for critical construction processes.</li>
  <li>Define inspections, tests, hold/witness points, acceptance criteria, and records.</li>
  <li>Link ITP checkpoints to lot management and verification records.</li>
</ul>
<h3>2.3 Permits &amp; Authorisations</h3>
<ul>
  <li>Obtain required permits (hot work, confined space, electrical) before work starts.</li>
  <li>Track permit status and close-out in the permits module.</li>
</ul>
<h2>3. Execution Controls</h2>
<ul>
  <li>Brief crews daily on scope, risks, and controls; record attendance.</li>
  <li>Ensure plant, equipment, and materials meet specification and inspection requirements.</li>
  <li>Coordinate subcontractors to follow company procedures and documentation.</li>
</ul>
<h2>4. Monitoring &amp; Verification</h2>
<ul>
  <li>Conduct routine inspections, supervision checks, and quality verifications.</li>
  <li>Record results in lot and ITP registers; manage NCRs promptly.</li>
  <li>Monitor KPI performance via project dashboards (safety, quality, productivity).</li>
</ul>
<h2>5. Change &amp; Deviation Management</h2>
<ul>
  <li>Submit change requests for scope/design deviations; obtain approvals before implementation.</li>
  <li>Document variations, impacts, and updated controls.</li></ul>
<h2>6. Records &amp; Integration</h2>
<ul>
  <li>Store SWMS, ITPs, permits, inspection records, and reports in document management with controlled access.</li>
  <li>Link operational controls to lots, NCRs, incidents, and improvement actions.</li>
  <li>Review operational control effectiveness during management review and audits.</li>
</ul>
'''
